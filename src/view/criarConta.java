    private void criarContaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarContaBtnActionPerformed
        // TODO add your handling code here:
        conn.conectar();
        String nome = fNome.getText();
        String email = fEmail.getText();
        String senha = fSenha.getText();
        String confirmSenha = fConfirmeSenha.getText();
        
        
        
        if(senha.equals(confirmSenha) && !conn.verificaEmail(email)){
        String sql = "INSERT INTO conta(nome, email, senha) VALUES ('"+nome+"','"+email+"','"+senha+"');";
        conn.manipulaSQL(sql);
        
        fNome.setText("");
        fEmail.setText("");
        fSenha.setText("");
        fConfirmeSenha.setText("");
        
        JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
        
        }else{
            JOptionPane.showMessageDialog(null, "E-mail existente ou senhas incompatÃ­veis");
        }
    
        
    }//GEN-LAST:event_criarContaBtnActionPerformed

